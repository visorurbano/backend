        <h5>¿Qué es Visor Urbano?</h5>
        <small>
            Visor Urbano es una plataforma digital de gestión urbana que contiene de manera digital las normas y planes de desarrollo urbano de la ciudad. De manera inmediata podrás consultar qué tipo de negocios y construcción se pueden realizar en el predio seleccionado.También cuenta con un área especializada de mapas donde podrás consultar los planes de desarrollo de la ciudad, todas las licencias de negocios y permisos de construcción emitidos, podrás acceder a la maqueta en 3D del centro de guadalajara, conocer las redes hidráulicas y el arbolado de la ciudad. Toda la información presentada en Visor Urbano será de consulta libre.
        </small>
        <br><br>
        <h5>¿Cómo funciona Visor Urbano?</h5>
        <small>
            Visor urbano es una plataforma digital que tiene toda la información referente a los planes de desarrollo de la ciudad, las normas vigentes de construcción y de licencias  para negocios de manera georreferenciada, es decir,  al seleccionar un predio la plataforma validará de manera directa los trámites que puedes comenzar a realizar en ese predio.
            <br><br>
            Con la información vertida en cada uno de los predios de la ciudad tendrás la información georreferenciada para su libre consulta, por lo que si tienes duda sobre si algún permiso o alguna licencia corresponde a la normativa vigente, puedes consultarlo en la plataforma y en caso de que no coincida lo permitido con lo que sucede, podrás levantar un reporte.
        </small>
        <br><br>
        <h5>¿Qué trámites puedo realizar en Visor Urbano?</h5>
        <small>
            En la primera versión de Visor urbano podrás realizar los siguientes trámites y consultas:
            <br>
            <ul>
                <li>Trámites de licencias para negocio tipo A.</li>
                <li>Permiso de construcción de hasta 500 m<sup>2</sup>.</li>
                <li>Renovación de licencias.</li>
                <li>Factibilidad de uso de suelo.</li>
                <li>Consultar los planes de desarrollo urbano vigentes.</li>
                <li>Inventario del patrimonio cultural edificado y monumentos del centro de Guadalajara.</li>
                <li>Reportes ciudadanos sobre irregularidades en negocios o construcciones.</li>
            </ul>
            <br>
            <b>Próximamente</b> podrás tramitar y consultar:
            <br>
            <ul>
                <li>Todos los  permisos de construcción.</li>
                <li>Licencias para negocios de los giros B, C y D.</li>
                <li>Patrimonio cultural edificado del Centro Histórico de Guadalajara y los barrios tradicionales.</li>
                <li>Módulo de Business Intelligence.</li>
                <li>Catastro en 3D</li>
            </ul>
        </small>
        <br>
        <h5>¿Visor Urbano solo funciona para los trámites de Guadalajara o la puedo usar para otros municipios?</h5>
        <small>
            Visor Urbano es una plataforma que se ha creado para atender las necesidades de los procesos de desarrollo urbano de la ciudad de Guadalajara, uno de los objetivos de la plataforma es el generar procesos estandarizados para lograr que sea replicable en otras ciudades. En ésta primera etapa de implementación sólo funcionará para los trámites en el municipio de Guadalajara
            Se está trabajando para integrar las necesidades normativas, legales y de desarrollo urbano de los municipios del Área metropolitana de Guadalajara para en un futuro poder implementar la plataforma en los municipios que la integran.
        </small>
        <br><br>
        <h5>¿Cuánto duran los trámites en Visor Urbano?</h5>
        <small>
            Los tiempos que duran los trámites son variados y estarán acordes a la normativa vigente. El usuario podrá supervisar en tiempo real el proceso y la oficina en la que se encuentra su trámite. De igual manera se notificará vía correo electrónico si se necesita realizar alguna modificación a los documentos presentados.
        </small>
        <br><br>
        <h5>¿Qué información de los predios se podrá consultar?</h5>
        <small>
            <ul>
                <li>Clave catastral.</li>
                <li>Superficie del terreno.</li>
                <li>Superficie construida.</li>
                <li>Medida del frente del predio.</li>
                <li>Estado de pago predial.</li>
                <li>Domicilio.</li>
                <li>Zonificación y usos de suelo.</li>
                <li>Tipo de negocios permitidos.</li>
                <li>Licencias de obra, de giro y su estatus.</li>
            </ul>
        </small>
        <br>
        <h5>¿Si no quiero realizar mi trámite en línea puedo acudir a realizar mi trámite en ventanilla?</h5>
        <small>
            La atención para el usuario que desee realizar su trámite en las oficinas correspondientes podrá hacerlo. El trámite realizado en ventanilla se realizará a través de la misma plataforma y bajo los mismos criterios que se implementarán para los trámites que puedes realizar desde tu casa.
        </small>
        <br><br>
        <h5>¿Por qué no me deja comenzar mi trámite en el predio seleccionado?</h5>
        <small>
            Cuando la plataforma no te permita iniciar un trámite quizá sea porque el predio que has seleccionado no tiene permitida la actividad de negocio o tipo de construcción que deseas realizar. Para mayor información consulta el área de contacto
        </small>
        <br><br>
        <h5>¿Qué puedo hacer si no puedo realizar el trámite que necesito  en el predio seleccionado?</h5>
        <small>
            Si la plataforma no te permite realizar algún trámite, quizá es porque la normativa vigente impide realizar ese tipo de actividad para esa área en específico. Para mayor información consulta el área de contacto
        </small>
        <br><br>
        <h5>¿Por qué necesito mi firma electrónica para finalizar el trámite de manera digital?</h5>
        <small>
            En México la única manera legal para certificar la identidad de una persona de forma electrónica es a través de la firma electrónica  del SAT. (citar ley) El mecanismo electrónico que implementa nos da la certeza de que el portador de los archivos y la clave es el firmante. La firma electrónica cuenta con la misma validez y eficacia jurídica que la rúbrica autógrafa, ya que en ambos casos representa la voluntad del firmante, lo anterior con fundamento en los artículos 1, 3, fracciones II, III y IV, 7, 8, 15, 16, del Reglamento del Uso de Medios Electrónicos del Ayuntamiento de Guadalajara, artículos 7, 8, 9, 11, 12, 13, 21, 22, de la Ley de Firma Electrónica Avanzada Para el Estado de Jalisco y sus Municipios.
        </small>
        <br><br>
        <h5>¿Puedo realizar el trámite digital si no tengo firma electrónica?</h5>
        <small>
            Los trámites no se pueden realizar de manera digital sin el uso de la firma electrónica,  por lo que recomendamos solicitar su firma electrónica. Para poder obtener la firma electrónica no necesariamente se tiene que tener una actividad fiscal activa.
            <br><br>
            De otro modo, puede acudir a las ventanillas especiales de Visor Urbano ubicadas en las direcciones de Obras Públicas y Padrón y Licencias, donde podrá ser auxiliado para realizar su trámite de manera presencial.
        </small>
        <br><br>
        <h5>¿Qué información se podrá consultar mediante mi e-Firma  del SAT?</h5>
        <small>
            La información a la que se tendrá acceso es la información que por ley puede ser consultada de manera pública: Nombre y RFC. De ninguna manera tendremos acceso ni enviaremos información referente a las contribuciones fiscales del firmante ya que dicha facultad únicamente es competencia del Servicio de Administración Tributaria.
        </small>
        <br><br>
        <h5>¿Cuál va a ser la utilización de la e-Firma del SAT?</h5>
        <small>
            La e-Firma, unicamente será utilizada como medio de identificación del solicitante de la licencia o permiso, el ayuntamiento de Guadalajara, no otorgará ningún tipo de información referente a la e-Firma a ninguna persona ni autoridad.
        </small>
        <br><br>
        <h5>Dónde puedo obtener mi firma electrónica?</h5>
        <small>
            Puedes obtenerlo en cualquier oficina del SAT. Para tramitarla necesitas los siguientes documentos: copia simple de la CURP, comprobante de domicilio, Unidad de memoria extraíble (USB), Correo electrónico. Para mayores informes: <a href="http://www.sat.gob.mx/fichas_tematicas/fiel/Paginas/obtener_fiel.aspx" target="_blank">http://www.sat.gob.mx/fichas_tematicas/fiel/Paginas/obtener_fiel.aspx</a>
        </small>
        <br><br>
        <h5>¿Qué validez tiene mi licencia emitida vía electrónica?</h5>
        <small>
            Las licencias y los permisos otorgados de manera electrónica tendrán la misma validez que los otorgados en ventanilla,  ya que contarán con elementos de seguridad para su autentificación de manera electrónica.
        </small>
        <br><br>
        <h5>¿Qué mecanismos de seguridad tiene mi permiso o licencia obtenida a través de visor urbano?</h5>
        <small>
            Cada uno de los trámites emitidos a través de la plataforma digital contendrá un código QR con información sobre el trámite realizado, mismo que sólo podrá ser leído mediante un software especial de la Dirección de Inspección y Vigilancia. Todos los trámites contarán con una firma electrónica impresa que validará la emisión y la información del trámite impreso.
        </small>
        <br><br>
        <h5>¿Puedo obtener mi licencia en papel oficial y con sello de la dependencia?</h5>
        <small>
            Los trámites que se gestionen de manera electrónica a través de Visor Urbano se podrán imprimir y contarán con elementos de seguridad que certificarán su autenticidad y tendrá el mismo valor que las formas valoradas emitidas en ventanilla.
            De igual manera podrás acudir a las oficinas para solicitar la impresión de tu trámite en papel certificado.
        </small>
        <br><br>
        <h5>¿Puedo tramitar un permiso de anuncio para el predio en el que estoy solicitando una licencia para negocio?</h5>
        <small>
            Si, solamente que la solicitud de licencia de anuncio se desarrollará en la segunda etapa de visor urbano. En la primera etapa, una vez obtenida la licencia para negocio se tiene que acudir a las oficinas correspondientes.
        </small>
        <br><br>
        <h5>¿Puedo comenzar un trámite y guardar el avance?</h5>
        <small>
            Si, cada de los trámites ingresados pero no finalizados tendrán una caducidad de 30 días para ingresar los documentos solicitados, de lo contrario el trámite de la factibilidad de uso de suelo se desechará y se tendrá que volver a consultar en línea.
        </small>
        <br><br>
        <h5>¿Puedo realizar varios trámites con mi mismo perfil?</h5>
        <small>
            Al registrarte como usuario  en la plataforma podrás identificarte como propietario o como gestor de trámites. A partir de ésta información podrás iniciar los trámites para los que cuentes  con los documentos probatorios y que certifiquen que cuentas con el poder legal de hacerlos a nombre del propietario.
        </small>
        <br><br>
        <h5>¿Que pasa si ya inicié mi trámite, cargué documentos y no cuento con la firma electrónica para certificar mi personalidad jurídica electrónica?</h5>
        <small>
            Todos los trámites iniciados con documentos cargados en la plataforma cuentan con 5 días hábiles para certificar el proceso con su firma electrónica. Si no se firma de manera electrónica en éste tiempo se desechará el trámite y tendrá que comenzar nuevamente el trámite de factibilidad de uso de suelo. De lo contrario puedes acudir a las oficinas donde se realiza tu trámite para finalizarlo de manera presencial.
        </small>
        <br><br>
        <h5>¿Qué sanciones tendré en el caso a anexar documentos y/o información falsa?</h5>
        <small>
            Las sanciones será la revocación de la licencia y por ende cierre del negocio, inicio de procedimiento administrativo en contra del titular de la licencia y del solicitante pudiendo dictarse incluso un arresto de hasta por 36 horas, y la denuncia ante la Fiscalía General del Estado de Jalisco por el delito de falsificación de documentos.
        </small>
        <br><br>
        <h5>¿Cómo se puede obtener una licencia en un predio donde el propietario ya falleció?</h5>
        <small>
            Es necesario que el albacea que fue reconocido dentro del juicio de la sucesión comparezca a solicitar el trámite o bien designe a un representante.
        </small>
        <br><br>
        <h5>¿Qué protección tendrán los datos y documentos que se envían a la plataforma?</h5>
        <small>
            La publicación de los datos personales tendrá como límite lo establecido en la Ley de Protección de Datos Personales en Posesión de Sujetos Obligados del Estado de Jalisco y sus Municipios y la Ley de Transparencia y Acceso a la Información Pública.
        </small>
        <br><br>
        <h5>¿Qué formatos de archivo se pueden subir a la Plataforma?</h5>
        <small>
            Los formatos aceptados por la plataforma son, para las imágenes; PNG, JPG,JPEG, y para los documentos es el PDF.
        </small>
